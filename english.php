        Hungarian worship software to project lyrics in church
    </div>
    <div class="text">
        This project was started on the summer of 2005, and has been used since that Christmas in our church <br />
        (<a target="blank" href="http://www.esztergomi-ersekseg.hu/?fm=1&am=4&op=plebaniaview&p_id=145">Budapest, Hungary, 
                Mátyásföld, Saint Joseph Roman Catholic Ecclesia</a>, 
            other links:
        <a target="blank" href="http://www.plebania.net/vendegszoba/?op=view&vid=180">Virtuális Plébánia</a> 
            and 
        <a target="blank" href="http://matyasfold.emecclesia.hu"> Mátyásföldi plébánia</a>). 
            This is a computer program which can utilize a computer projector to help singing and praying the service participants by 
            showing religion lyrics and pictures.
        <p>
            The program is completely free, but we would like to get an <a href="mailto:info@diatar.eu">email</a> from possible users to 
            know about them and to help them installing the system, or to let them know about updates.
        </p>
        <p>
            You can also post some reply in <a href="?page=forum&f=5">our forum</a>.
						Although there are mostly hungarian posts, you can write english posts, too.
        </p>       
        <p>
            The program is for Hungarian catholic churches, because its language is Hungarian, the complete documentation is 
            Hungarian, and the lyrics-collections are mainly from the Hungarian catholic and protestant song treasury. 
            But of course it is free to use for any other target: e.g. the Taize-songs or some Gregorian or pictures, 
            or if somebody import other lyrics or write in free texts...
        </p>
    </div>
    <div class="iconed_title">
        Main features
    </div>
    <div class="dotted_text">
        <ul>
					<li> freeware program</li>
          <li> versions for Windows or Linux</li>
          <li> utilizes dual screen (extended desktop) to make independent the projection and controls</li>
          <li> one computer to control and to project, or individual computers for cantor and for projection
			(in this latter case the second also can be used alone to control and to project)</li>
          <li> using two computers the program is able to organize two projections to serve schola or other assistants with their special needs</li>
          <li> many song-collections, now Unicode (UTF8) capable</li>    
          <li> the collected song orders for the ceremony can be saved and reloaded as needed, even automatically loaded at a given date</li>
          <li> projected picture can be rotated and mirrored, with resizable black borders, and also horizontal keystone is possible</li>
          <li> the projector can be automatically switched on/off using serial connection</li>
          <li> external control buttons (on/off, step, etc.) applicable through serial or parallel port</li>
          <li> settable colors, font type and size, but usually automatically resized to fit text</li>
          <li> some simple formatting capabilities (underline, bold, italic, non-breaking space)</li>
					<li> moveable word highlight to help participate those hard of hearing</li>
          <li> external lyrics editor, which helps development for the community</li>
          <li> capable to play sound during projection, with automatic forward</li>
          <li> automatic timed forward, common and individual showing properties to help audio-visual presentations</li>
					<li> showing guitar-accords and/or notes above lyrics</li>
        </ul>

    
