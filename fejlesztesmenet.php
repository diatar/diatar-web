        <p>
            A program Borland Delphi 5.0, később CodeGear Delphi 2007 fejlesztőkörnyezetben íródott. Mivel felmerült az 
            igény a Linux alapú használatra, Hoze átírta <a target="_top" href="http://www.lazarus.freepascal.org/index.php">Lazarus</a> alá, 
            amely egy Free Pascal-on alapuló ingyenes, grafikus fejlesztőkörnyezet. A Lazarus-projekt 
            kimondott célja, hogy segítségével Object Pascal és Delphi programot 
            lehessen írni olyan módon, hogy az azután egy újrafordítást követően többféle 
            operációs rendszeren is futhasson. Így a kód nagy részének megmentésével 
            lehetővé vált a program Windows és Linux rendszeren futó 
            változatainak egyidejű fejlesztése.</p>
