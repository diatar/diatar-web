<p>E dokumentum tárgya a "Diatár" nevű templomi kivetítő program,
annak a Windows, Linux ill. Android (vagy bármely más) operációs rendszeren futó változatai,
beleértve az Androidos változat "Diatár" és "DiaVetítő" részeit is (továbbiakban "Diatár");
valamint a <a href="https://diatar.eu">https://diatar.eu</a> nevű honlap (továbbiakban "Honlap").</p>
<br/>
<p><b><u>DIATÁR:</u></b></p>
<p>A Diatár szabadon letölthető, szabad szoftver (GNU GPLv3),
mely a működése során nem kér, nem analizál és nem is gyűjt vagy oszt meg semmiféle személyes adatot.
Forrás kódja a Honlapról - akár ellenőrzés céljából is - letölthető.</p>
<p>Az un. két-gépes üzemmódban a Diatár "Vezérlő" része - a felhasználó kifejezett utasítására –
átküldi a kivetítendő adatokat a Vetítő gépre, hogy az megjelenítse azokat.
Természetesen amennyiben olyan diákat választ ki, melyek személyes adatokat tartalmaznak,
akkor azok a vetítés idejére átkerülnek a Vetítő gépre, de ott tárolásra vagy feldolgozásra nem kerülnek.</p>
<p>A Diatár egyik verziója sem használ olyan program-könyvtárakat,
amelyek külső hirdetéseket tartalmaznak vagy adatokat küldenek, osztanak meg harmadik féllel;
sőt a program működése nem is igényel internet kapcsolatot.</p>
<p>Ha a Diatár egyik Androidos változata hiba miatt leáll,
akkor un. Crash-data keletkezik annak technikai részleteivel.
Ezt a felhasználó feltöltheti (elküldheti) a fejlesztőknek, mellyel segíti a hiba kijavítását.
Ez a hibajelentés nem a Diatár, hanem az Android op.rendszer és/vagy a Google szolgáltatása.
Az elküldött adatok hivatalos listája ezen a linken érhető el: <a href="https://github.com/ACRA/acra/wiki/ReportContent">https://github.com/ACRA/acra/wiki/ReportContent</a></p>
<p>Amennyiben az Androidos verziót nem a Honlapról, hanem a Google Play-ből tölti le vagy frissíti,
úgy a Google birtokába juthat bizonyos személyes adatainak.</p>
