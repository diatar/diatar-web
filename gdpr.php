<p>E dokumentum tárgya a "Diatár" nevű templomi kivetítő program,
annak a Windows, Linux ill. Android (vagy bármely más) operációs rendszeren futó változatai,
beleértve az Androidos változat "Diatár" és "DiaVetítő" részeit is (továbbiakban "Diatár");
valamint a <a href="https://diatar.eu">https://diatar.eu</a> nevű honlap (továbbiakban "Honlap").</p>
<br/>
<p><b><u>DIATÁR:</u></b></p>
<p>A Diatár szabadon letölthető, szabad szoftver (GNU GPLv3),
mely a működése során nem kér, nem analizál és nem is gyűjt vagy oszt meg semmiféle személyes adatot. Ez alól egyetlen kivétel az 'internetes' vetítés adminisztrációjához használt felhasználó név, jelszó és e-mail cím, ahol az utóbbit kizárólag a biztonsági megerősítő kód kiküldésére használja. A Diatár a bejelentkezési adatokat kódolva tárolja, de ha maximális biztonságra törekszik akkor kapcsolja ki a név és jelszó megjegyzését. A jelszó a szerveren biztonságosan, egyirányúan kódolva van tárolva. A Diatár forráskódja az internetről (https://github.com/diatar) - akár ellenőrzés céljából is - letölthető.</p>
<p>Az un. két-gépes üzemmódban a Diatár "Vezérlő" része - a felhasználó kifejezett utasítására –
átküldi a kivetítendő adatokat a Vetítő gépre, hogy az megjelenítse azokat.
Természetesen amennyiben olyan diákat választ ki (vagy ír be), melyek ad abszurdum személyes adatokat tartalmaznak,
akkor azok a vetítés idejére átkerülnek a Vetítő gépre, de ott tárolásra vagy feldolgozásra nem kerülnek.</p>
<p>A Diatár egyik verziója sem használ olyan program-könyvtárakat,
amelyek külső hirdetéseket tartalmaznak vagy adatokat küldenek, osztanak meg harmadik féllel;
sőt a program hagyományos működése nem is igényel internet kapcsolatot.
Ez alól kivétel:<br/>
- az Androidos Diatár énektár letöltési funkciója,<br/>
- az 'internetes' vetítési mód, ahol a diák továbbítása a nyílt interneten keresztül történik,<br/>
- ill. a zsolozsmák letöltése, amikor évente egyszer a Diatár a breviar.sk szerverhez fordul.</p>
<p>Ha a Diatár egyik Androidos változata hiba miatt leáll,
akkor un. Crash-data keletkezik annak technikai részleteivel.
Ezt a felhasználó feltöltheti (elküldheti) a fejlesztőknek, mellyel segíti a hiba kijavítását.
Ez a hibajelentés nem a Diatár, hanem az Android op. rendszer és/vagy a Google szolgáltatása.
Az elküldött adatok hivatalos listája ezen a linken érhető el: <a href="https://github.com/ACRA/acra/wiki/ReportContent" target=_BLANK>https://github.com/ACRA/acra/wiki/ReportContent</a></p>
<p>Amennyiben az Androidos verziót nem a Honlapról, hanem a Google Play-ből tölti le vagy frissíti,
