minden böngészés névtelenül, "látogatóként" történik.</p>
<p>Látogatása során számítógépe a weboldal működéséhez szükséges sütiket (cookies) kaphat.
Ezek arra hivatottak, hogy javítsák az oldal böngészésekor tapasztalt élményt.
A sütik elfogadása nem kötelező, azokat a böngészőjén keresztül letilthatja és törölheti is.
A sütikről bővebben például az <a href="https://allaboutcookies.org">allaboutcookies.org</a> oldalon olvashat angolul.</p>
<p>A Honlap böngészése során eljutnak hozzánk bizonyos személyes adatai is a böngésző-program jóvoltából,
mint például az IP-címe, látogatásának ideje, a megtekintett oldalak címe és a számítógépén használt szoftver környezete.
Ezekre a Honlap kényelmes és biztonságos működésének biztosítása érdekében van szükség.
Ezeket az adatokat a webtárhely szolgáltató szervere tárolhatja, majd 90 nap után automatikusan törli.
A Honlap a Magyar Hosting GDPR kompatibilis szolgáltatását veszi igénybe.</p>
