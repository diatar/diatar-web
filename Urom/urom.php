			Szent György templomukat 1764-66 között kibvítik,
			1936-ban pedig a régi templomot a szentély irányában nagyméretű új hajóval toldják meg.
			Pilisborosjenő filiája 1803-ig, utána helyi lelkészség.
			1821 óta plébánia. 1946-ban a község tiszta sváb és katolikus lakosságának
			97,5%-át kitelepítették és helyükbe Szabolcsból Tarpáról reformátusokat,
			Szigetmonostorról vegyesvallásúakat, Mezőkövesdről katolikusokat telepítettek.
			1986-ban a reformátusok svájci támogatással templomot és lelkészlakot építettek.
			A községben jelentős számban vannak még baptisták, jehovisták és néhány nazarénus család,
			igen sok (10-12) gyerekkel. Az egyházközség 1993-óta tartozik az esztergom-budapesti főegyházmegyéhez.
