        <p> 
			2019. elején az ürömi katolikus templom híveiben is felmerült az igény,
			hogy jobban követhető lenne a liturgia, egységesebben énekelhetnénk az énekeket,
			ha azokat kivetítéssel a vetítővásznon tudnák a hívek követni.
		</p>
		<p>
			A kapott magas összegű árajánlatokat mérlegelve elhatároztuk,
			hogy önerőből igyekszünk a kiépítést megvalósítani. 
        </p> 
		<p>
			A templom elülső részének tagolt elrendezése megnehezítette a kiépítés megvalósítását.
			Közeli falfelület, ahova vetíthetnénk, nem állt rendelkezésre.
			Az egyik oldalon a szószék, a másikon pedig a tabernákulum van,
			amik fölött már nincs megfelelő méretű falfelület és a vetítési szög sem lett volna ideális.
			Végül a képen látható helyre, pontosabban véglegesen még kb. fél méterrel afölé
			(ameddig a boltív engedte) kellett felszerelnünk a vetítővásznat,
			mert ha lejjebb raktuk, akkor egyes helyekről nem volt a vetített kép teljes mértékben
			látható a belógások miatt. Ezt a vásznat be tudtuk volna vetíteni hátulról is,
			de plébános atya kérése az volt, hogy lehetőleg ne helyezzünk el projektort a szentélyben.
			A vászonnak mi gyártottunk egy fém keretet, amit zsanérokkal a falra rögzítettünk,
			így ha nincs rá szükség be lehet hajtani és elölről nem látható.
		</p>
		<br/>
		<p>
			A laptop és a projektor összekötésére az volt alapvető elképzelésünk,
			hogy vezeték nélküli megoldással oldanánk meg a kapcsolatot.
			A magas és hosszú templom bevezetékezése hálókábelekkel, ráadásul mindezt esztétikusan is megoldva,
			nem lett volna egyszerű feladat. Alapvetően hátul fent az orgonához a kántornak kell egy hozzáférési pont,
			de mivel nálunk rendszeres gitáros misék is vannak,
			a gitáros misék alatt a kórus elöl és lent helyezkedik el.
			(Az egyik kórustag vezérli a kivetítést ilyenkor).
			Erre először egy Acer vezetéknélküli display adaptert próbáltunk ki,
			de az nem tudta kezelni a Windows kiterjesztés módját,
			ami alapvetően szükséges a Diatár program megfelelő működéséhez.
			A megfelelő megoldás a Microsoft wireless display adapter lett,
			és az egyébként nem kis méretű templom bármely pontjáról minőségromlás nélkül
			tudunk vetíteni wifi-n keresztül a laptopról a projektorra.
		</p>
