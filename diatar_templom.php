        <p> 
            Korábban a sekrestye egyik polcán tároltuk a sok száz diát különböző dobozokban. Mivel mindig kevés volt az idő mise előtt, gyakran  
            csak a doboz fedelébe kerültek a tárból a felesleges kockák. Aztán egy idő után már semmit se lehetett megtalálni, mert minden dia 
            egy rakáson kavargott a dobozfedélben. Ilyenkor már csak azokból az énekekből válogattunk, amiket hirtelenjében megtaláltunk.
            </p> 
        <p>
             A diák berakásának külön mozdulatsora volt: jobb kézzel a számozásnál felemeljük, a fény felé tartva ellenőrizzük a szövegét, azután 
            csavart mozdulattal a fekete felét fordítjuk magunk felé, és számozással lefelé behelyezzük. Az ünnepek súlyával egyenes arányban nőtt 
            annak a veszélye, hogy valamelyik dia fordítva kerül a tárba. De az is előfordult, hogy a buzgó segítő a nagy sietségben megbotlott, 
            és az összes diát szétszórta a templom kövén. Az üvegek eltörtek, a kockák összekavarodtak... Abban az évben különösen 
            emlékezetes volt az ünnep!
        </p>
        <p>
            Májusban mindig időt kellett hagyni a litánia és a mise között, mert a sok versszak nem fért el egy tárban, le kellett szaladni 
            cserélni. Sokszor bosszankodtam azon is, hogy a vasárnapi diákmisére bekészített énekeket szombaton nem tudom vetíteni, 
            ha nem akarok hozzányúlni az összeállított énekrendhez.
        </p>
